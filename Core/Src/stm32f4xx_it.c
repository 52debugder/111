/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
  static unsigned char Timer_state = 0;   	 //任务调度软件计数器
	static unsigned char Dis_Flag = 0;			//获取距离的计数器
  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
  if(Timer_state == 0)
		{
			if(Dis_Flag == 0)
			{
				UT_GetDistance();//启动超声波（此函数似乎并不能直接获取距离）
			}
			MpuGetData();//读取陀螺仪数据加滤波
			RC_Analy();	//通道数据处理
			
			Dis_Flag++;
			Dis_Flag %=3;
		}
		if(Timer_state == 1)
		{

			GetAngle(&MPU6050,&Angle,0.003f);//角度解析
//			if(Dis_Flag == 2)
//			{
				Get_Location();	//获取实际高度
//			}
		}
		if(Timer_state == 2)
		{
			//控制模式选择:遥控/自动  ---正面右上角拨码开关
			if(Remote.AUX1 == 200)    
			{
				FlightPidControl_RC(0.003f);	//飞行控制
			}
			else if(Remote.AUX1 == 1800)
			{
				FlightPidControl_Auto(0.003f);   //飞机控制方式-自动/遥控
			}
			
			MotorControl();//电机控制
		}
		
		Timer_state++;    
		Timer_state%=3;
  /* USER CODE END TIM3_IRQn 1 */
}

